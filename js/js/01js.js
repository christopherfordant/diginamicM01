// déclaration d'une variable
// La déclaration permet de créer la variable en lui donnant un nom
{
  let i = 12;
  console.log(`i :`, i);
  console.log(`type de i :`, typeof (i));

  // affectation d'une valeur avec typage dynamique
  i = 12;
  console.log(`i :`, i);
  console.log(`type de i :`, typeof (i));

  i = "Hello World";
  console.log(`i :`, i);
  console.log(`type de i :`, typeof (i));

  i = true;
  console.log(`i :`, i);
  console.log(`type de i :`, typeof (i));

  i = null;
  console.log(`i :`, i);
  console.log(`type de i :`, typeof (i));

  i = 56;

}
